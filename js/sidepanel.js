function openPanel() {
    document.getElementById("mySidepanel").style.width = "60%";
    document.getElementById("mySidepanel").style.padding = "60px";
  }
  
  function closePanel() {
    document.getElementById("mySidepanel").style.width = "0";
    document.getElementById("mySidepanel").style.padding = "0px";
  }
  